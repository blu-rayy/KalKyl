export function BootScreen({ onDone }) {
  const [fading, setFading] = useState(false)

  function dismiss() {
    if (fading) return
    setFading(true)
    setTimeout(onDone, 700)
  }

  useEffect(() => {
    const onKey = () => dismiss()
    window.addEventListener('keydown', onKey)
    return () => window.removeEventListener('keydown', onKey)
  }, [fading])

  return (
    <div
      onClick={dismiss}
      style={{
        position: 'fixed',
        inset: 0,
        background: '#000',
        display: 'flex',
        flexDirection: 'column',
        alignItems: 'center',
        justifyContent: 'center',
        zIndex: 99999,
        opacity: fading ? 0 : 1,
        transition: 'opacity 0.7s ease',
        cursor: 'pointer',
        userSelect: 'none',
      }}
    >
      <pre style={{
        fontFamily: '"Courier New", Courier, monospace',
        fontSize: 'clamp(6px, 0.85vw, 11px)',
        color: '#00ff88',
        lineHeight: 1.25,
        whiteSpace: 'pre',
        margin: 0,
        textShadow: '0 0 8px rgba(0,255,136,0.5)',
      }}>
        {ASCII_ART}
      </pre>

      <p style={{
        marginTop: '28px',
        fontFamily: '"Courier New", Courier, monospace',
        fontSize: '12px',
        color: '#00ff88',
        letterSpacing: '0.2em',
        animation: 'kk-blink 1.1s step-start infinite',
        textShadow: '0 0 6px rgba(0,255,136,0.6)',
      }}>
        PRESS ANY KEY TO CONTINUE
      </p>
    </div>
  )
}
